const COLOR_THEME_KEY = "color-theme";

/** Returns the id of the currently saved color theme (or null = default). */
export function getSavedColorTheme(): string | null {
  if (typeof window === "undefined") return null;
  return localStorage.getItem(COLOR_THEME_KEY);
}

/** Builds the CSS string that overrides :root and .dark for a given theme. */
function buildThemeCSS(theme: ColorTheme): string {
  const rootVars = Object.entries(theme.light)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join("\n");
  const darkVars = Object.entries(theme.dark)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join("\n");
  return `:root {\n${rootVars}\n}\n.dark {\n${darkVars}\n}`;
}

/** Injects (or removes) the theme override <style> tag. */
export function applyColorTheme(id: string | null): void {
  const STYLE_ID = "color-theme-override";
  let el = document.getElementById(STYLE_ID) as HTMLStyleElement | null;

  if (!id) {
    el?.remove();
    localStorage.removeItem(COLOR_THEME_KEY);
  } else {
    const theme = COLOR_THEMES.find((t) => t.id === id);
    if (theme) {
      if (!el) {
        el = document.createElement("style");
        el.id = STYLE_ID;
        document.head.appendChild(el);
      }
      el.textContent = buildThemeCSS(theme);
      localStorage.setItem(COLOR_THEME_KEY, id);
    }
  }
}

/** Call once on app bootstrap to restore a persisted theme. */
export function restoreColorTheme(): void {
  const id = getSavedColorTheme();
  if (id) applyColorTheme(id);
}
